#!/usr/bin/env bash
#
# Bootstrap a fresh EC2 instance (AL2023 arm64) into a working Vespa node for
# the comparative benchmark. Idempotent — safe to re-run.
#
# Usage (from the new Vespa EC2 box):
#   1. scp the entire vespa-app directory (including this script) onto the box
#      scp -r ~/workplace/opensearch-benchmark/docker/vespa-app ec2-user@<ip>:~/
#   2. ssh into the box
#      ssh ec2-user@<ip>
#   3. bash ~/vespa-app/bootstrap.sh
#
# After completion:
#   - Docker installed + running
#   - 'vespa' container running vespaengine/vespa image
#   - target_index schema deployed, cluster healthy
#   - Port 8080 (search) and 19071 (config server) bound
#
set -euo pipefail

log() { printf '\033[1;34m[bootstrap]\033[0m %s\n' "$*"; }
err() { printf '\033[1;31m[bootstrap ERROR]\033[0m %s\n' "$*" >&2; }

APP_DIR="$(cd "$(dirname "$0")" && pwd)"
log "Using application package: $APP_DIR"

if [[ ! -f "$APP_DIR/services.xml" ]]; then
    err "services.xml not found in $APP_DIR — is this the right directory?"
    exit 1
fi

# ----- 0. Generate validation-overrides.xml with a fresh 'until' date -----
# Vespa's config server rejects 'until' dates more than 30 days in the future,
# so we regenerate this file at every deploy with a date 28 days out. This
# lets us apply destructive schema changes (e.g. distance-metric on an indexed
# vector field) during benchmarking iteration without manual edits.
FUTURE_DATE=$(date -u -d '+28 days' +%Y-%m-%d 2>/dev/null || date -u -v +28d +%Y-%m-%d)
log "Generating validation-overrides.xml with until=$FUTURE_DATE"
cat > "$APP_DIR/validation-overrides.xml" <<EOF
<?xml version="1.0" encoding="UTF-8"?>
<!--
  Auto-generated by bootstrap.sh. Vespa enforces a 30-day cap on 'until' dates;
  re-run the bootstrap before this expires to refresh.
-->
<validation-overrides>
    <allow until="$FUTURE_DATE">indexing-change</allow>
    <allow until="$FUTURE_DATE">field-type-change</allow>
    <allow until="$FUTURE_DATE">tensor-type-change</allow>
    <allow until="$FUTURE_DATE">indexing-mode-change</allow>
</validation-overrides>
EOF

# ----- 1. Install Docker (AL2023 arm64) -----
if ! command -v docker >/dev/null 2>&1; then
    log "Installing Docker..."
    sudo dnf install -y docker
    sudo systemctl enable --now docker
    sudo usermod -aG docker ec2-user
    log "Docker installed. NOTE: if this is your first run, log out and back in for group membership to take effect, or use 'sudo docker' for the remaining commands."
fi

# Use sudo for docker if the current user isn't in the docker group yet
if docker ps >/dev/null 2>&1; then
    DOCKER="docker"
else
    log "Current user not in docker group yet — using sudo for this run"
    DOCKER="sudo docker"
fi

# ----- 2. Start the Vespa container -----
if $DOCKER ps --format '{{.Names}}' | grep -q '^vespa$'; then
    log "Vespa container already running — leaving it alone"
elif $DOCKER ps -a --format '{{.Names}}' | grep -q '^vespa$'; then
    log "Vespa container exists but is stopped — starting it"
    $DOCKER start vespa
else
    log "Pulling and starting Vespa container..."
    $DOCKER run -d --name vespa \
        -p 8080:8080 \
        -p 19071:19071 \
        --ulimit nofile=262144:262144 \
        vespaengine/vespa
fi

# ----- 3. Wait for config server -----
log "Waiting for Vespa config server on :19071..."
for i in {1..60}; do
    if curl -sf http://localhost:19071/state/v1/health >/dev/null 2>&1; then
        log "Config server is up."
        break
    fi
    if (( i == 60 )); then
        err "Config server did not come up within 2 minutes."
        $DOCKER logs --tail 50 vespa || true
        exit 1
    fi
    sleep 2
done
sleep 3  # extra settle

# ----- 4. Copy the app package into the container -----
log "Copying application package into the container..."
$DOCKER exec vespa rm -rf /tmp/vespa-app 2>/dev/null || true
$DOCKER cp "$APP_DIR" vespa:/tmp/vespa-app

# ----- 5. Deploy via vespa-deploy (prepare + activate) -----
log "Preparing application..."
$DOCKER exec vespa /opt/vespa/bin/vespa-deploy prepare /tmp/vespa-app

log "Activating application..."
$DOCKER exec vespa /opt/vespa/bin/vespa-deploy activate

# ----- 6. Wait for content cluster -----
log "Waiting for content cluster to report healthy..."
for i in {1..60}; do
    state=$($DOCKER exec vespa vespa-get-cluster-state 2>&1 || true)
    if echo "$state" | grep -q "content/storage/0: up" && \
       echo "$state" | grep -q "content/distributor/0: up"; then
        log "Content cluster healthy:"
        echo "$state" | grep -E "content/(storage|distributor)"
        break
    fi
    if (( i == 60 )); then
        err "Content cluster did not become healthy within 2 minutes."
        echo "$state"
        exit 1
    fi
    sleep 2
done

# ----- 7. Verify the deployed schema has the expected metric -----
log "Verifying deployed target_index.sd..."
deployed_metric=$($DOCKER exec vespa sh -c '
    find /opt/vespa/var/db/vespa/config_server/serverdb/tenants/default/sessions/ \
        -name target_index.sd 2>/dev/null |
    xargs grep -h distance-metric 2>/dev/null |
    tail -1 |
    sed -E "s/.*distance-metric: *//; s/[ ;].*//"
')
log "Deployed distance-metric: ${deployed_metric:-<not found>}"

local_metric=$(grep -E "^\s*distance-metric:" "$APP_DIR/schemas/target_index.sd" | awk '{print $2}')
if [[ "$deployed_metric" != "$local_metric" ]]; then
    err "Mismatch: local schema says '$local_metric' but deployed says '$deployed_metric'"
    exit 1
fi

# ----- 8. Smoke-test the search endpoint -----
# Vespa's search container on port 8080 initializes AFTER the content cluster
# reports healthy — can take another 30-90 seconds on a fresh deploy. Poll
# until it's ready rather than checking once and giving up.
log "Waiting for search container on :8080 (up to 3 minutes)..."
for i in {1..90}; do
    if curl -sf http://localhost:8080/ApplicationStatus >/dev/null 2>&1; then
        version=$(curl -s http://localhost:8080/ApplicationStatus | python3 -c 'import sys,json; print(json.load(sys.stdin)["application"]["vespa"]["version"])' 2>/dev/null || echo "?")
        log "Vespa $version is live and serving on :8080"
        break
    fi
    if (( i == 90 )); then
        err "Search endpoint did not come up within 3 minutes."
        err "Check 'docker logs vespa' for startup errors."
        exit 1
    fi
    sleep 2
done

log "Bootstrap complete. This node is ready for benchmarking."
log ""
log "From the runner box, point your OSB command at: http://$(hostname -I | awk '{print $1}'):8080"
log ""
log "To re-deploy after editing schemas:"
log "  $DOCKER cp $APP_DIR vespa:/tmp/vespa-app"
log "  $DOCKER exec vespa /opt/vespa/bin/vespa-deploy prepare /tmp/vespa-app"
log "  $DOCKER exec vespa /opt/vespa/bin/vespa-deploy activate"
